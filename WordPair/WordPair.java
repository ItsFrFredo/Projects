package WordPair;

public class WordPair {
	
	private String First;
	private String Second;
	
	public WordPair( String first, String second) {
		this.First = first;
		this.Second = second;
	}
	
	public String getFirst() {
		return First;
	}
	
	public String getSecond() {
		return Second;
	}
	
	
}
